package com.pretask;

public class SimpleLoopExample {
  public static void main(String[] args) {
    // - класс SimpleLoopExample, объявите целочисленную переменную и используйте цикл for, чтобы
    // вывести числа от 1 до этой переменной;
    int myVar = 7;
    for (int i = 1; i < myVar; i++) {
      System.out.println(i);
    }
  }
}
