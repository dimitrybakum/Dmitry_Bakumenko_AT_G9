package pretask;

public class MathOperatorsExample {
  public static void main(String[] args) {
    // - класс MathOperatorsExample, объявите две целочисленные переменные и выполните операции
    // сложения, вычитания, умножения и деления, выведите результаты на экран;
    int a = 44;
    int b = 22;

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);

    int sum = a + b;
    int sub = a - b;
    int mul = a * b;
    int div = a / b;
    System.out.println(sum);
    System.out.println(sub);
    System.out.println(mul);
    System.out.println(div);
  }
}
